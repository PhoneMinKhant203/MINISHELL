#include "../minishell.h"

void	free2p(char **s)
{
	int	i;

	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		free(s[i]);
		i++;
	}
	free(s);
}

void	free1p(char **s)
{
	if (s && *s)
	{
		free(*s);
		*s = NULL;
	}
}
